package com.ecoinnova.pe.model.util;

import java.io.Serializable;
import java.math.BigDecimal;

public class DetalleBoletaReporte implements Serializable{
	
	private static final long serialVersionUID = 1L;
	
	private String     nombreProducto;
	private BigDecimal precioBol;
	private Integer    cantidadBol;
	private BigDecimal importe;
	
	public String getNombreProducto() {
		return nombreProducto;
	}
	public void setNombreProducto(String nombreProducto) {
		this.nombreProducto = nombreProducto;
	}
	public BigDecimal getPrecioBol() {
		return precioBol;
	}
	public void setPrecioBol(BigDecimal precioBol) {
		this.precioBol = precioBol;
	}
	public Integer getCantidadBol() {
		return cantidadBol;
	}
	public void setCantidadBol(Integer cantidadBol) {
		this.cantidadBol = cantidadBol;
	}
	public BigDecimal getImporte() {
		return importe;
	}
	public void setImporte(BigDecimal importe) {
		this.importe = importe;
	}
	
	
	

}
