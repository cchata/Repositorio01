package com.ecoinnova.pe.model.util;

import java.io.Serializable;

public class HeaderBoletaReporte implements Serializable {
	
	private static final long serialVersionUID = 1L;
	
	private String numeroBoleta;
	private String fechBoleta;
	private String razonSocialCliente;
	public String getNumeroBoleta() {
		return numeroBoleta;
	}
	public void setNumeroBoleta(String numeroBoleta) {
		this.numeroBoleta = numeroBoleta;
	}
	public String getFechBoleta() {
		return fechBoleta;
	}
	public void setFechBoleta(String fechBoleta) {
		this.fechBoleta = fechBoleta;
	}
	public String getRazonSocialCliente() {
		return razonSocialCliente;
	}
	public void setRazonSocialCliente(String razonSocialCliente) {
		this.razonSocialCliente = razonSocialCliente;
	}
	
	

}
